/**
 * Cartesian coordinates of a vector using the standard basis.
 */
interface VectorE3 {
    /**
     * The Cartesian x-coordinate.
     */
    readonly x: number;

    /**
     * The Cartesian y-coordinate.
     */
    readonly y: number;

    /**
     * The Cartesian z-coordinate.
     */
    readonly z: number;
}

export default VectorE3;
